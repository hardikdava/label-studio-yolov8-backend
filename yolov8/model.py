class YOLOv8Model(LabelStudioMLBase):
    def __init__(self, **kwargs):
        # Call base class constructor
        super(YOLOv8Model, self).__init__(**kwargs)

        from_name, schema = list(self.parsed_label_config.items())[0]
        self.from_name = from_name
        self.to_name = schema['to_name'][0]
        self.labels = []
        for key, val in category_map.items():
            self.labels.append(val)
        self.model = YOLO("yolov8s.pt")

    def predict(self, tasks, **kwargs):
        """ This is where inference happens: model returns
            the list of predictions based on input list of tasks
        """
        task = tasks[0]

        predictions = []
        score = 0

        img_path = task["data"]["image"]
        image_path = self.get_local_path(img_path)
        img = Image.open(image_path)
        original_width, original_height = get_image_size(image_path)

        results = self.model.predict(img)

        for result in results:
            for i, prediction in enumerate(result.boxes):
                xyxy = prediction.xyxy[0].tolist()
                predictions.append({
                    "id": str(i),
                    "from_name": self.from_name,
                    "to_name": self.to_name,
                    "type": "rectanglelabels",
                    "score": prediction.conf.item(),
                    "original_width": original_width,
                    "original_height": original_height,
                    "image_rotation": 0,
                    "value": {
                        "rotation": 0,
                        "x": xyxy[0] / original_width * 100,
                        "y": xyxy[1] / original_height * 100,
                        "width": (xyxy[2] - xyxy[0]) / original_width * 100,
                        "height": (xyxy[3] - xyxy[1]) / original_height * 100,
                        "rectanglelabels": [category_map[int(prediction.cls.item())]]
                    }
                })
                score += prediction.conf.item()

        return [{
            "result": predictions,
            "score": score / (i + 1),
            "model_version": "v8s",  # all predictions will be differentiated by model version
        }]
